    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            st = cn.createStatement();
            if (txtNIM.getText().equals("") ||
                    txtNama.getText().equals("") ||
                    txtProdi.getText().equals("") ||
                    txtEmail.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Data tidak boleh kosong", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            if (btnSave.getText() == "Save"){
               String cek = "SELECT * FROM data_mahasiswa WHERE NIM = '" + txtNIM.getText() + "'";
               rs = st.executeQuery(cek);
               if (rs.next()) {
                   JOptionPane.showMessageDialog(null, "Oops....NIM ini sudah ada");
               }else{
                   String sql = "INSERT INTO data_mahasiswa VALUES ('" + txtNIM.getText() + 
                           "','" + txtNama.getText() + 
                           "','" + txtProdi.getText() + 
                           "','" + txtEmail.getText() + "')";
                   st.executeUpdate(sql);
                   JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                   Bersih();
                   TampilData();
               }
            }else{
                String update = "UPDATE data_mahasiswa SET Nama = '" + txtNama.getText() + 
                        "', Prodi = '" + txtProdi.getText() + 
                        "', Email = '" + txtEmail.getText() + 
                        "' WHERE NIM = '" + txtNIM.getText() + "'";
                
                st.executeUpdate(update);
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                Bersih();
                TampilData();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    
